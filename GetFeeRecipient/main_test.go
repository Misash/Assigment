func Test1(t *testing.T) {

	data := []string{
		"0x455e5aa18469bc6ccef49594645666c587a3571b",
		"0x455e5aa18469bc6ccef49594645666c587a3571b",
		"0x999e5aa81352ty6zzei49594645666p599a2993c",
		"0x999e5aa18469bc6ccef49594645666c587a3571b",
		"0x454e5aa18469bc6ccef49594645666c587a3571b",
		"0x666e5aa18469bc6ccef49594645666c587a3571b",
		"0x455e5aa18469bc6ccef49594645666c587a3571b",
		"0x455e5aa18469bc6ccef49594645666c587a3571b",
	}

	got := GetUniqueFeeRecipients(data)

	want := []string{
		"0x455e5aa18469bc6ccef49594645666c587a3571b",
		"0x999e5aa81352ty6zzei49594645666p599a2993c",
		"0x999e5aa18469bc6ccef49594645666c587a3571b",
		"0x454e5aa18469bc6ccef49594645666c587a3571b",
		"0x666e5aa18469bc6ccef49594645666c587a3571b",
	}

	if !reflect.DeepEqual(got, want) {
		t.Errorf("got %q, wanted %q", got, want)
	}

}


